package basic;

public class Switch {

	public static void main(String[] args) {
		//1 2 3 4 5 6 7
		int a=3;
		switch(a)
		{
		case 1:
			System.out.println("sunday");
			break;
		case 2:
			System.out.println("monday");
			break;
		case 3:
			System.out.println("tuesday");
			break;
		case 4:
			System.out.println("wednesday");
			break;
			default:
				System.out.println("invalid day");
			
			
		}

	}

}
