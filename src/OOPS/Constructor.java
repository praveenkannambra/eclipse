package OOPS;

public class Constructor {

		int empid;
		int salary;
		String designation;
		
		
		public Constructor()
		{
			empid =4;
			salary=10000;
			designation="developer";
		}
	
		
		
		public static void main(String[] args) {
			
			
			Constructor ob=new Constructor();
			System.out.println(ob.empid);
			System.out.println(ob.salary);
			System.out.println(ob.designation);
		}

}
			
		
   
		




